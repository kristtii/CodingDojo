# first_name - Mark, last_name - Guillen
# first_name - KB, last_name - Tonel


def iterateDictionary(dict):
    for element in dict:
        output = ""
        for key, value in element.items():
            output += f"{key} - {value}, "
        print(output)


iterateDictionary(students)

